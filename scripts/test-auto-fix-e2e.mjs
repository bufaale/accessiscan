#!/usr/bin/env node
// End-to-end smoke test for the AccessiScan GitHub App.
//
// Pre-reqs (one-time, manual):
//   1. Install the AccessiScan Auto-Fix App on the bufaale/test-auto-fix-sandbox repo:
//        https://github.com/apps/accessiscan-auto-fix/installations/new
//      (select "Only select repositories" → test-auto-fix-sandbox)
//   2. Export from .shared/.env.keys:
//        export GITHUB_ACCESSISCAN_AUTO_FIX_APP_ID=3502257
//        export GITHUB_ACCESSISCAN_PRIVATE_KEY_PATH=/path/to/private-key.pem
//      (or paste the literal key into GITHUB_ACCESSISCAN_PRIVATE_KEY)
//
// Run: node scripts/test-auto-fix-e2e.mjs
//
// What it does:
//   - Authenticates as the GitHub App
//   - Looks up the installation on bufaale/test-auto-fix-sandbox
//   - Creates a branch + a markdown file at accessiscan-fixes/e2e-<ts>.md
//   - Opens a PR titled "AccessiScan E2E smoke test"
//   - Prints the PR URL
//
// Cleanup: close the PR + delete the branch from the repo UI.

import { createAppAuth } from "@octokit/auth-app";
import { Octokit } from "@octokit/rest";
import { readFileSync } from "node:fs";

const REPO_OWNER = "bufaale";
const REPO_NAME = "test-auto-fix-sandbox";
const REPO_FULL = `${REPO_OWNER}/${REPO_NAME}`;

function loadPrivateKey() {
  const literal = process.env.GITHUB_ACCESSISCAN_PRIVATE_KEY;
  if (literal && literal.includes("BEGIN")) return literal;
  const path = process.env.GITHUB_ACCESSISCAN_PRIVATE_KEY_PATH;
  if (path) return readFileSync(path, "utf8");
  throw new Error(
    "Missing GITHUB_ACCESSISCAN_PRIVATE_KEY or GITHUB_ACCESSISCAN_PRIVATE_KEY_PATH env",
  );
}

async function main() {
  const appId = process.env.GITHUB_ACCESSISCAN_AUTO_FIX_APP_ID || "3502257";
  const privateKey = loadPrivateKey();

  const auth = createAppAuth({ appId, privateKey });

  // Step 1: app-level auth → list installations.
  const appOctokit = new Octokit({
    authStrategy: createAppAuth,
    auth: { appId, privateKey },
  });

  console.log(`[1/5] Looking up installation on ${REPO_FULL}...`);
  const { data: install } = await appOctokit.rest.apps.getRepoInstallation({
    owner: REPO_OWNER,
    repo: REPO_NAME,
  });
  console.log(`      Found installation_id=${install.id} account=${install.account.login}`);

  // Step 2: installation token.
  console.log(`[2/5] Minting installation token...`);
  const installAuth = await auth({ type: "installation", installationId: install.id });
  const octokit = new Octokit({ auth: installAuth.token });

  // Step 3: get default branch sha.
  console.log(`[3/5] Reading default branch...`);
  const { data: repo } = await octokit.rest.repos.get({ owner: REPO_OWNER, repo: REPO_NAME });
  const baseBranch = repo.default_branch;
  const { data: ref } = await octokit.rest.git.getRef({
    owner: REPO_OWNER,
    repo: REPO_NAME,
    ref: `heads/${baseBranch}`,
  });
  const baseSha = ref.object.sha;
  console.log(`      base=${baseBranch} sha=${baseSha.substring(0, 7)}`);

  // Step 4: create a new branch + commit a markdown file.
  const ts = Date.now();
  const branch = `accessiscan/e2e-smoke-${ts}`;
  console.log(`[4/5] Creating branch ${branch} + committing fix-report...`);
  await octokit.rest.git.createRef({
    owner: REPO_OWNER,
    repo: REPO_NAME,
    ref: `refs/heads/${branch}`,
    sha: baseSha,
  });
  const filePath = `accessiscan-fixes/e2e-${ts}.md`;
  const content = `# AccessiScan E2E smoke

Generated by \`scripts/test-auto-fix-e2e.mjs\` at ${new Date().toISOString()}.

This file confirms the AccessiScan GitHub App can:
- Authenticate as the App
- Resolve its installation on \`${REPO_FULL}\`
- Create branches and open PRs

If this PR exists, the App credentials in \`.shared/.env.keys\` are valid and the
Auto-Fix Phase 1 plumbing is end-to-end functional.
`;
  await octokit.rest.repos.createOrUpdateFileContents({
    owner: REPO_OWNER,
    repo: REPO_NAME,
    path: filePath,
    message: "test: AccessiScan E2E smoke",
    content: Buffer.from(content, "utf8").toString("base64"),
    branch,
  });

  // Step 5: open the PR.
  console.log(`[5/5] Opening PR...`);
  const { data: pr } = await octokit.rest.pulls.create({
    owner: REPO_OWNER,
    repo: REPO_NAME,
    title: "AccessiScan E2E smoke test",
    head: branch,
    base: baseBranch,
    body:
      `Automated PR opened by \`scripts/test-auto-fix-e2e.mjs\`.\n\n` +
      `**What this proves**: the AccessiScan GitHub App is correctly installed on \`${REPO_FULL}\`, ` +
      `the App ID + private key from \`.shared/.env.keys\` work, and the Octokit client can mint ` +
      `installation tokens + open PRs.\n\n` +
      `**Cleanup**: close this PR + delete the branch.`,
  });

  console.log("");
  console.log(`SUCCESS — PR opened:`);
  console.log(`  ${pr.html_url}`);
  console.log("");
  console.log("Cleanup: close the PR + delete the branch when done.");
}

main().catch((err) => {
  console.error("FAILED:", err.message);
  if (err.status) console.error("HTTP status:", err.status);
  if (err.response?.data) console.error("Response:", JSON.stringify(err.response.data, null, 2));
  process.exit(1);
});
